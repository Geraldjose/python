def calculadora(n1, n2, operator):
    if operator == "*":
        print(n1 * n2)
    elif operator == "+":
        print(n1 + n2)
    elif operator == "-":
        print(n1 - n2)
    elif operator == "/":
        print(n1 / n2)



calculadora(5, 6, "*")
calculadora(5, 6, "+")
calculadora(5, 6, "-")
calculadora(5, 6, "/")
